    fn check_count_of_required_peers(current: usize, low: usize, high: usize) {
        if low > high {
            return;
        }
        let required = super::count_of_required_peers(current, low, high);
        if current <= high {
            assert!((low..=high).contains(&(current + required)));
        } else {
            assert_eq!(required, 0);
        }
    }

